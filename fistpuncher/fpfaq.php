Q: Why is Fist Puncher local co-op only?  Isn't that for losers?
<br><br>
A: We're a small studio (just the 2 of us) so we had to make some difficult choices when it came to the game's features.  We didn't have the resources to do it right, so we didn't want to mess it up. 
<br><br>
Besides, we think local co-op is the superior co-op.  Make friends, go to other human being's homes, share food and conversation while you play videogames.  Or, if you already have friends or family, consider them as local co-op partners.  What's better than a little face-to-face human interaction now and then?
