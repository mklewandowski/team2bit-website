<b>Fist Puncher FAQ</b>
<br><br><br>

Q: Wasn't there a Fist Puncher Kickstarter?
<br><br>
A: In April of 2012, we raised $23,723 on Kickstarter to fund development of Fist Puncher.
<br><br>
<a href="http://www.kickstarter.com/projects/928099324/fist-puncher-old-school-beat-em-up">Fist Puncher Kickstarter</a>
<br><br><br><br>
Q: What's the story with the $10k backer on the Kickstarter campaign?   
<br><br>
A: Someone did pledge $10k to have us make a game for them, but that game is not Fist Puncher.  It's another beat 'em up called Escape From Hizbollia."  It's finished but we're holding it back for later this year.
<br><br><br><br>
Q: Are there real people in Fist Puncher?
<br><br>A: Some of the hidden playable characters and NPCs are based on real people who supported our Kickstarter campaign.  There is also signage from real small businesses, from a local kettle corn company to other videogames.  We try to work with like minded indies in all lines of business.
<br><br>
One of our backers is Dracogen, Inc., an investment company that has funded several Double Fine Projects.  You can even play as Dracogen and XE.com founder Steven Dengler.
